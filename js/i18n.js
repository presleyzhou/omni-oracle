const OO_LANGS = [
  ["en", "English"],
  ["zh", "中文"],
  ["fr", "Français"],
  ["es", "Español"],
  ["ko", "한국어"],
];

let OO_LANG = localStorage.getItem("oo-lang") || "en";
if (!I18N[OO_LANG]) OO_LANG = "en";

function OO_T(key) {
  return (I18N[OO_LANG] && I18N[OO_LANG][key]) || I18N.en[key] || key;
}

function applyI18n() {
  document.documentElement.lang = OO_LANG === "zh" ? "zh-CN" : OO_LANG;
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    el.innerHTML = OO_T(el.dataset.i18n);
  });
  document.dispatchEvent(new CustomEvent("oo:lang"));
}

(function initI18n() {
  const nav = document.querySelector(".nav-inner");
  if (nav && !document.getElementById("langSel")) {
    const sel = document.createElement("select");
    sel.id = "langSel";
    sel.className = "lang-select";
    sel.setAttribute("aria-label", "Language / 语言");
    sel.innerHTML = OO_LANGS.map(([v, l]) => `<option value="${v}">${l}</option>`).join("");
    sel.value = OO_LANG;
    sel.addEventListener("change", () => {
      OO_LANG = sel.value;
      localStorage.setItem("oo-lang", OO_LANG);
      applyI18n();
    });
    nav.appendChild(sel);
  }
  applyI18n();
})();
